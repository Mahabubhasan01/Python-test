"""
greater than >
grater than or equal >=
less than <
less than equal <=
equal to equal ==
not equal !=
"""

first_value = 100
second_value = 50
if first_value > second_value :
    print('first greater than second')

if first_value >= second_value :
    print('first big or equal')

if first_value < second_value :
    print('first small or equal')
if first_value <= second_value :
    print('first is not big or equal')

if first_value == second_value :
    print('those are not equal')


